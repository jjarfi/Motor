package com.example.jarfi.sispak.Database.Model;

public class ModelGejala {

    String id;
    String nama;

    public ModelGejala() {
        this.id = id;
        this.nama = nama;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }
}